#' Is a graph connected?
#'
#' @description
#'
#' Is a graph connected?
#'
#' @param laplacian_matrix A Laplacian matrix (where the diagonal is the degree matrix and the off-diagonal the inverse of the adjacency matrix).
#'
#' @details
#'
#' Example graph:
#'
#' \preformatted{6---4---5
#'     |   |\
#'     |   | 1
#'     |   |/
#'     3---2}
#'
#' Has the adjacency matrix:
#'
#' \preformatted{    _________________________
#'     | 1 | 2 | 3 | 4 | 5 | 6 |
#' -----------------------------
#' | 1 | 0 | 1 | 0 | 0 | 1 | 0 |
#' -----------------------------
#' | 2 | 1 | 0 | 1 | 0 | 1 | 0 |
#' -----------------------------
#' | 3 | 0 | 1 | 0 | 1 | 0 | 0 |
#' -----------------------------
#' | 4 | 0 | 0 | 1 | 0 | 1 | 1 |
#' -----------------------------
#' | 5 | 1 | 1 | 0 | 1 | 0 | 0 |
#' -----------------------------
#' | 6 | 0 | 0 | 0 | 1 | 0 | 0 |
#' -----------------------------}
#'
#' And the degree matrix:
#'
#' \preformatted{    _________________________
#'     | 1 | 2 | 3 | 4 | 5 | 6 |
#' -----------------------------
#' | 1 | 2 | 0 | 0 | 0 | 0 | 0 |
#' -----------------------------
#' | 2 | 0 | 3 | 0 | 0 | 0 | 0 |
#' -----------------------------
#' | 3 | 0 | 0 | 2 | 0 | 0 | 0 |
#' -----------------------------
#' | 4 | 0 | 0 | 0 | 3 | 0 | 0 |
#' -----------------------------
#' | 5 | 0 | 0 | 0 | 0 | 3 | 0 |
#' -----------------------------
#' | 6 | 0 | 0 | 0 | 0 | 0 | 1 |
#' -----------------------------}
#'
#' And the Laplacian matrix:
#'
#' \preformatted{    _________________________
#'     | 1 | 2 | 3 | 4 | 5 | 6 |
#' -----------------------------
#' | 1 | 2 |-1 | 0 | 0 |-1 | 0 |
#' -----------------------------
#' | 2 |-1 | 3 |-1 | 0 |-1 | 0 |
#' -----------------------------
#' | 3 | 0 |-1 | 2 |-1 | 0 | 0 |
#' -----------------------------
#' | 4 | 0 | 0 |-1 | 3 |-1 |-1 |
#' -----------------------------
#' | 5 |-1 |-1 | 0 |-1 | 3 | 0 |
#' -----------------------------
#' | 6 | 0 | 0 | 0 |-1 | 0 | 1 |
#' -----------------------------}
#'
#' @return A logical (TRUE or FALSE).
#'
#' @author Graeme T. Lloyd \email{graemetlloyd@@gmail.com}
#'
#' @examples
#'
#' x <- matrix(
#'   data = c(
#'      2, -1,  0,  0, -1,  0,
#'     -1,  3, -1,  0, -1,  0,
#'      0, -1,  2, -1,  0,  0,
#'      0,  0, -1,  3, -1, -1,
#'     -1, -1,  0, -1,  3,  0,
#'      0,  0,  0, -1,  0,  1
#'   ),
#'   ncol = 6,
#'   byrow = TRUE
#' )
#'
#' # Check graph is connected:
#' is_graph_connected(laplacian_matrix = x)
#'
#' @export is_graph_connected
is_graph_connected <- function(laplacian_matrix) {
  
  #
  eigen(x = laplacian_matrix)$values[2] > 0
}
